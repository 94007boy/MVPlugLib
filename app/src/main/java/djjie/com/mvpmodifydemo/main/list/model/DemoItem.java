package djjie.com.mvpmodifydemo.main.list.model;

public class DemoItem {

    private String nick_name;
    private String avatar;
    private String goodComment;
    private String deal_count;
    private String bro_score;
    private String create_time;
    private String show_brief;

    public String getNick_name() {
        return nick_name;
    }

    public void setNick_name(String nick_name) {
        this.nick_name = nick_name;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getGoodComment() {
        return goodComment;
    }

    public void setGoodComment(String goodComment) {
        this.goodComment = goodComment;
    }

    public String getDeal_count() {
        return deal_count;
    }

    public void setDeal_count(String deal_count) {
        this.deal_count = deal_count;
    }

    public String getBro_score() {
        return bro_score;
    }

    public void setBro_score(String bro_score) {
        this.bro_score = bro_score;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public String getShow_brief() {
        return show_brief;
    }

    public void setShow_brief(String show_brief) {
        this.show_brief = show_brief;
    }
}
